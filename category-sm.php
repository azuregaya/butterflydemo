                                    <h6>管理图纸及文件</h6>
                                    <p>解决企业中复杂的电子图纸、软件、文件、技术资料、相关标准、更改记录等数据的归档、查询、共用、换版等控制问题，对图纸的创建、审批、归档、发布、回收、报废等生命周期状态进行高效的管控，各种状态之间的转换通过审批流程的控制，自动保存审批记录、变更记录以及旧版资料并实现自动关联，以保证资料的完整一致性，确保图纸资料的正确使用。</p>
                                    <p>各种类型的CAD2D/3D机械结构图纸、EDA原理图/PCB图、以及各种异构类型的文件都能在K/3  PLM中统一组织、管理、使用和共享，被授权的用户可以方便地互相参考、借用甚至是同步开发，所有的数据都确保其唯一性和在多处使用时的一致性。产品开发效率和产品开发质量将得到提高。</p>
