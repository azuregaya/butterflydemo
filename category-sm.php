                                    <h6>企业产品开发过程的困扰</h6>
                                    <p> 经过国家多年的大力信息化推广，国内企业的信息化应用不断扩展和深入，不少企业实施应用了ERP、CRM、OA、CAD、CAE、CAM等信息化系统，企业管理得到提升。但在产品开发过程中经常出现以下困扰：</p>
                                    <h6>企业产品研发管理的滞后影响产品生产管理</h6>
                                    <p>很多企业实施应用了ERP，覆盖企业的销售、计划、采购、生产、财务等业务，企业的物流和资金流得到有效管控，管理有了很大的提升。但随着应用的深入，往往会发现企业对产品研发这个生产制造的源头缺乏有效管控，产品研发周期难以控制，图纸、BOM等数据不准，导致ERP系统不能正常发挥作用。产品研发信息化管理的严重滞后，成为企业发展的最大瓶颈。</p>
                                    <h6>先进的研发管理思想的贯彻执行方面</h6>
                                    <p>大部分的企业都通过ISO9000等质量体系认证，有的甚至实施IPD（集成产品开发）体系，但往往达不到预期的效果。制定的业务流程效率很低甚至难以执行，项目组织与项目流程难以协调，制定的管理规范很难贯彻，使得ISO、IPD等先进管理体系没有发挥出作用。</p>
                                    <h6>产品开发协同管理方面</h6>
                                    <p>产品开发的主体是研发部门，不能与营销、采购、制造等部门建立有效的协同机制，对客户、市场的响应较慢。<br>
                                        没有明确的产品开发流程，职责不明，或有流程但没有有效的系统控制手段而执行较差，每个人按照各自的习惯做事，产品开发的品质控制很难到位。<br>
                                        没有信息共享的工作平台，开展并行工程困难，开发周期长；很难详细掌握各开发项目的进度，每个人在做什么，做到什么程度。<br>
                                        没有有效的手段进行绩效管理，做好做坏没有明显分别（至少没有让员工真正有动力），所以缺乏阶段性的及时激励、奖惩等，导致企业无法通过绩效管理将压力层层分解和传递，无法支撑企业战略目标的实现。<br>
                                        与合作伙伴缺乏异地协同工作的信息平台，协调困难，工作效率低且质量难以控制。<br>
                                        没有及时了解管理部门物料价格、库存、质量等信息，以便合理选择原材料、配套件和优化设计，不利于降低和控制产品成本以及提高设计质量。
                                    </p>
                                    
                                    <h6>产品数据管理及应用方面</h6>
                                    <p>
                                        人工处理各种信息工作量大，容易出错。<br>
                                        产品电子资料分散存放，缺乏统一管理工具。围绕产品研发，每个部门都产生了数量巨大、类型各异的业务电子资料，如CAD图纸、产品设计更改通知、产品配置表、ISO文件、质量检测报告、实验报告等，并以海量的速度增加。这些电子数据完全依靠Windows的目录功能管理和组织电子资料文件，出现了“有电子化数据，无电子化管理”的局面，割裂了产品数据的关联性，既降低了企业知识的整体价值，也大大增加资料的管理难度。<br>
                                        无法严密控制版本，各部门及供应商使用的技术资料由于种种原因，会出现版本不一致的情况，直接造成经济损失，交货期拖延。<br>
                                        电子数据安全得不到保障，容易流失和被非法修改。<br>
                                        没有在设计源头有效地控制新物料的增长，随着开发人员的增加，物料呈爆炸性增长态势，随之带来的成本增加及质量不稳定。<br>
                                        电子数据安全得不到保障，容易流失和被非法修改。<br>
                                        产品数据难以科学积累，很多都掌握在员工个体手上，员工的离职对企业影响很大。<br>
                                        研发与管理部门互为信息孤岛，管理部门产品生产数据重复录入，产品数据准备周期长，质量难以保证，极易影响产品按期交付。
                                    </p>
