                                    <p>
                                    集团集中采购解决方案支持常见的三种集中采购应用模式：
                                    <br>
                                        <em>• 集中定价，分散采购：</em>集团统一选择、认证供应商，并统一维护供应商关系，指定供应商的供应内容、价格和配额。但具体采购行为、采购时间由各使用机构具体确定和执行。这种应用场景，最大限度地协调了集团企业多组织的频繁采购与集中管理之间的矛盾；<br>
                                        <em>• 集中采购，集中配送：</em>集团内各组织、机构的采购需求，由集团统一进行采购，并要求供应商交货到指定的物流中心或仓库，根据采购条款，按照发票对供应商进行结算。收货后商品或物料，根据存货策略和需求，由集团进行统一的物流配送。集中采购，集中配送模式，侧重规模优势和先进的管理水平，达到最大的供应效率；<br>
                                        <em>• 集中采购，分开收货：</em>与集中采购、集中配送的区别在于，集团统一采购的商品或物料，由供应商直接送货到具体使用的机构或组织，由使用机构或组织进行收货、入库处理。采购结算可能由集团统一结算，也可能由各机构或组织，根据收货情况，分别单独与供应商进行结算。
