                                        组织能力模型的构建可以确保组织、人才与公司的战略方向协调一致。<br>
                                        员工能力：提升员工能力，解决员工会不会的问题，即企业需要员工具备什么样的能力素质，并引进和培养合适人才<br>
                                        员工思维：引导员工思维，解决员工愿不愿意的问题，即在能力具备的情况下是否愿意发挥其能力，与组织共同发展。<br>
                                        员工治理：优化员工治理，解决容不容许的问题，当找到合适有意愿的员工后，公司是否有一个保障他发挥创意和做出贡献的良好机制和环境。
