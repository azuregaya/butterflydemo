                                    <h6>品牌历程</h6>
                                    <p>金蝶s-HR（金蝶strategic Human Resources Management），是战略人力资源管理与IT整合的全面解决方案。金蝶s-HR的全面解决方案是基于战略人力资源管理理念，以增值人力资本、提升组织效能，推动战略执行、创造卓越绩效为人力资源战略驱动力，帮助企业实现整合人力资源服务，为企业业务战略提供支持。</p>
                                    <p>1998年，最基础的HR模块出现在金蝶的财务系统中，2001年，金蝶在香港联交所创业版挂牌上市，并在同年推出K/3 HR系统；2002年，K/3 HR成为中国第一家荣获国家人事部推荐使用证书的企业。2003年后，金蝶s-HR陆续推出了基于能力模型的战略人力资源管理解决方案（K3HR v10.0）及基于更加开放的集成与应用平台（新的技术架构遵循国际化的TOGAF指导下的SOA标准，融合了大量中国企业BPM实践；可实现企业云计算，促进IT构架与业务的相互融合、协同工作，致力于为企业构建更敏捷的IT架构，快速适应变化、按需部署）的集团人力资源管理解决方案（EAS HR）。金蝶s-HR根据客户反馈、市场环境和HR管理发展需要，不断地磨砺产品和完善解决方案，推陈出新。用户和市场也给予了金蝶s-HR积极的反馈和荣誉。</p>
                                    <p>2010年，为了更好的满足企业需求，金蝶推出“金蝶s-HR”品牌，整合K/3 HR与EAS HR解决方案，以中国战略人力资源管理解决方案第一品牌亮相，解决方案以“战略人力资源管理”为理念，将支持企业战略实现的组织能力贯穿到人力资源管理中，建立基于战略发展的有计划的人力资源管理模式，确保组织与公司的战略方向协调一致，并通过改善员工治理，提升员工能力，牵引员工思维来构建企业高效的组织能力，支持企业战略目标的实现。随着云计算的普及应用，金蝶s-HR计划在2012年推出人力资源云管理的解决方案，以帮助企业更高效的实现战略落地。</p>
