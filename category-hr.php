                                    <h6>产品理念</h6>
                                    <p>金蝶s-HR产品，以“战略人力资源管理”为理念，将支持企业战略实现的组织能力贯穿到人力资源管理中，建立基于战略发展的有计划的人力资源管理模式，确保组织与公司的战略方向协调一致，并通过改善员工治理，提升员工能力，牵引员工思维来构建企业高效的组织能力，支持企业战略目标的实现。
