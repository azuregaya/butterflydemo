                                    <p><em>用户对信息的需求</em>并非所有的企业管理者都需要同样组织方式、展现形式的业务信息，不同的管理需求带来不同的分析应用要求：</p>
                                    <p><em>决策型用户：</em>需要少量的关键业务信息（通常不超过12项）、模型化的分析路径、直观的分析图形、预警式的分析报表；</p>
                                    <p><em>分析型用户：</em>需要了解企业全价值链的经营要素、多样化的分析模型、灵活多变的展现方式、功能强大的OLAP(联机分析处理工具)；</p>
                                    <p><em>查询型用户：</em>需要基本固定的报表与信息、适用的分析模型、能够贯穿到最细粒度动因的多层级钻取分析工具；</p>
                                    <p><em>操作型用户：</em>业务流程触发的OLAP(联机分析处理工具)工具、明确的操作建议、与操作建议相关的业务信息简单展现；<br>
                                    以上各类型用户实际上并非严格划分，如企业高层管理者，往往既是决策型用户也是查询型用户，对于个别分析内容甚至扮演分析型用户的角色。</p>
                                    <p><em>分析信息的架构组成 </em>如何满足以上4种类型用户的需求，同时保证信息全面准确、关键业务信息选择的正确，是企业业务信息分析体系建立过程中最大的难题，金蝶KBI解决方案通过归纳总结信息类型，搭建全面绩效管理信息架构来完成这一任务：</p>
                                    <p><em>经营要素分析库：</em>根据企业整体价值链或分析部分价值链的全过程整理归纳，归纳总结其中每一个价值节点，从而得出企业经营要素，经营要素的所有应用分析展现方式的集合，形成企业经营要素分析库，企业的信息需求展现形式可能由于人员变更或目标变更而改变，但只要商业模式没有改变，经营要素分析库是不会改变的，经营要素分析库是企业经营信息的蓄水池，是企业管理中十分重要的知识总结平台；</p>
                                    <p><em>运营分析：</em>将经营要素分析库中的经营要素，以不同的应用分析形式展现出来，形成的非结构性的分析体系，就是运营分析体系，运营分析体系的特点是各种分析要素之间没有必然的联系，而是通过经验组织在一起，所以经常会因人而建、因人而废；</p>
                                    <p><em>解决方案模型：</em>将经营要素结构化的组织在一起，根据价值链全面展现其中的每一个环节，从而形成的分析模型成为解决方案分析模型，解决方案分析模型可以是关注整个企业商业模型的，也可以是只覆盖部分价值链，如只关心销售动因的价值链分析模型，解决方案分析模型主要是分析型用户维护并使用的，结构化的模型组织方式可以避免因为信息的缺失而对管理行为做出的误导；</p>
                                    <p><em>决策支持模型：</em>决策支持模型在归纳总结解决方案模型的基础上，加强了部分工具内容，使模型使用者与分析体系见出现互动，如模型的模拟预测、根据不同标杆值做出的对标差异分析等，决策支持模型往往作为企业管理理念最终体现方式，将企业商业模式中最重要的经营要素以结构化或非结构化模型的方式展现出来，最终达到提升企业绩效的目的；<br>
                                        以上4种信息的组成方式综合在一起，形成企业级商业智能分析解决方案。在应用过程中，可以根据用户需求任意组合，为企业绩效的提升带来真正价值。</p>
