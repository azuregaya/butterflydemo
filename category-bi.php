                                    <p>中国企业信息化建设近20年来有了长足的进步，面对日新月异的市场变化和不断增长的海量业务数据，企业决策者迫切希望将业务数据迅速转化成为对市场、对当前运营状况的认识，从而对现状进行深入洞察与分析，不断优化决策管理流程，确保企业持续的竞争力和不断超越自己的能力。</p>
                                    <p>商业智能（Business Intelligence，BI）就是能够系统地、即时地、方便地将企业运营相关的电子数据转化为企业运营优化与管理决策所需的关键业务信息的技术,是对商业信息的收集、管理和分析的过程，目的是使企业的各级决策者获得决策支持体系和洞察力，帮助他们更快地做出对企业有利的决策。现代企业需要基业长青、获取竞争力，离不开商业智能。</p>
                                    <p>目前，除了金融、电信、零售等信息化建设相对成熟的行业对商业智能的需求继续保持着快速增长外，集团型企业和大型制造行业，如钢铁、汽车、机械等，以及能源、化工、交通行业的增长也比较明显，甚至于中小企业也开始有了使用商业智能的需求。商业智能的应用和发展正以极高速的趋势在向各个领域覆盖，同时，显示出势不可挡的健康局面。</p>
                                    <p>金蝶KBI商业智能解决方案，关注企业管理用户的分析价值需要，在庞杂的业务信息中分辨关键业务信息，为企业各级管理人员提供最优经营分析模型、决策分析路径、最细粒度的差异动因查询，成为企业管理者在工作中最好的数据抓手和信息分析助手，帮助企业管理者做到“洞察秋毫，高效决策”。</p>
